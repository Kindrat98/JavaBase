package oop;

public class Sum {

    public int sum(int a, int b){
        return a + b;
    }

    public double sum(double a, double b){
        return a + b;
    }

    public String sum(String s1, String s2) {
        return s1 + s2;
    }

    public double sum(int a, double b){
        return a + b;
    }

    public int sum(double a, int b){
        return (int)(a + b);
    }
}
